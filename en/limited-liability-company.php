                            <div class="fancy-title left-align"><h1>Limited liability Company (Sàrl)</h1></div>
                            <div class="services-wrap">
                                <p>According to articles 772-827 of the Code of Obligations, a limited liability company is considered as a capital company, it may be created by one or more natural persons (individuals)or legal entities or both: debts are only guaranteed by the total assets of the company.</p>
                                <p>The creation of a limited liability company requires a minimum capital of CHF 20,000, not to mention that the costs of creation, which vary between CHF 2,500 and CHF 4,500, must go through a notary, it should be noted that registration in the commercial register is mandatory and must be done where it has its main office, and branches must also be registered where they are located.</p>
                                <p>A limited liability company shall only acquire legal personality at the time of its entry in the commercial register.</p>
                                <div class="fancy-title left-align"><h2>What are the advantages and disadvantages of the Sàrl? </h2></div>
                                <h3 class="section-title sub">Advantages:</h3>
                                <ul>
                                    <li>Liability is limited to the company's capital.</li>
                                    <li>Can be created by a single natural person (individual) or legal entity.</li>
                                    <li>The freedom to choose the name of the company (the mention "SARL" is mandatory). </li>
                                    <li>Possibility of changing the legal status to a limited company. </li>
                                    <li>Minimum capital of CHF 20,000. </li>
                                    <li>The partner(s) may receive a salary that is considered a burden on the company, which automatically reduces the tax burden on the limited liability company. </li>
                                    <li>The profit received from the sale of shares is not taxable. </li>
                                    <li>Possibility of increasing the capital by adding investors. </li>
                                </ul>
                                <h3 class="section-title sub">Disadvantages:</h3>
                                <ul>
                                    <li>Double taxation (tax on the profit and capital of the limited liability company and then tax on the income from dividends and capital for the holder(s) of the company's shares).</li>
                                    <li>The costs of setting up the limited liability company (between CHF 2500 and 4500).</li>
                                    <li>The publication in the commercial register of the identity and capital of the owner(s) of the limited liability company. </li>
                                    <li>Higher management fees than a sole proprietorship or partnership. </li>
                                    <li>No right to unemployment benefits for owners of the SARL, unless they leave the company permanently.</li>
                                </ul>
                                <h3 class="section-title sub">Limited Company (SA):</h3>
                                <p>The choice of a limited company is more suitable for large and medium-sized companies with capital exceeding CHF 100,000. The biggest difference between a limited company and a private limited company is the initial capital and the anonymity of the shareholders.</p>
                                <p>According to Article 620 of the Swiss Code of Obligations, a limited company is formed under a company name whose share capital is determined in advance and divided into shares, the debts of the limited company are guaranteed only by the total assets of the company.</p>
                                <p>A limited company may be created by one or more natural person (individual) or legal entity, or it may also be created for a non-profit-making purpose.</p>
                                <p>The limited company has the right to issue two types of shares: registered and bearer shares. Registered shares are entered in the share register of the company concerned, i.e. the company knows the shareholder's surname, first name, date of birth and address: for bearer shares, they are not in the name of the holder, which means that the company does not know who holds the bearer share.</p>
                                <p>As with a private limited company, registration of the limited company in the commercial register where it has its registered office is mandatory, branches must in turn be registered in the commercial register of the place where they are located.</p>
                                <p>The limited company acquires legal personality only at the time of its entry in the commercial register.</p>
                                <div class="fancy-title left-align"><h2>What are the advantages and disadvantages of SA?</h2></div>
                                <h3 class="section-title sub">Advantages:</h3>
                                <ul>
                                    <li>The shareholders' liability is limited to the company's capital.</li>
                                    <li>Total anonymity of shareholders.</li>
                                    <li>Shareholders working in the company are considered as employees, which gives them the right to be insured and to access social benefits. </li>
                                    <li>The mitigation of taxable profit by giving salaries to shareholders working directly or indirectly in the company.</li>
                                    <li>The profit received from the sale of shares is not taxable. </li>
                                    <li>Possibility of increasing the capital by adding investors. </li>
                                    <li>Greater influence of the founder.</li>
                                </ul>
                                <h3 class="section-title sub">Disadvantages:</h3>
                                <ul>
                                    <li>Very high minimum capital of CHF 100,000. </li>
                                    <li>Many formalities at the time of foundation. </li>
                                    <li>The costs of setting up the public limited company between CHF 3,000 and 7,000.</li>
                                    <li>Double taxation (tax on the profit and capital of the public limited company, then tax on income and dividends and capital for the holder or holders of the shares of the public limited company. </li>
                                    <li>Higher management costs compared to other legal entities.</li>
                                    <li>More accounting requirements: reserve, measure in the event of over-indebtedness. </li>
                                    <li>The management (board of directors and management) is responsible for the personal assets in the event of offence or negligence. </li>
                                </ul>
