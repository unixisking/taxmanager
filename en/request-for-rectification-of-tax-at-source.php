                                    <p>According to Articles 83-84 of the Federal Law on Direct Federal Tax, holders of a B, L, F, N, permit with those who are engaged in a lucrative activity without any swiss residence permit are automatically taxed at source. The employer has the obligation to collect the tax at source based on gross income by adding other existing payments such as: allowances, commissions, allocations, bonuses, shares, tips ... Then send it to the cantonal administration contribution according to the scale imposed by each canton. The tax rates are applicable depending on the family situation of each taxpayer.</p>
                                    <p>The rates are different from one package to the other, since the liability for direct federal income tax is applicable throughout Switzerland. This variation is explained by the sovereignty of the cantons that apply the right rate for their own tax policy.</p>
                                    <p>Nevertheless, this taxation is not final in all the cantons. The taxpayer has the right to apply for rectification of tax at source before March 31st of each fiscal year; in order to claim a partial refund of the tax at source, the taxpayer must in this case prove the existence of certain deductions that exceed the fixed price set up by the canton., Examples: transport costs, meals out of home, repurchase of 2nd and 3rd pillar, children or dependents, alimony and childcare.</p>
                                    <p><strong>Attention:</strong> the request for the correction of the tax at source does not guarantee in any case the partial refund of the tax already taken by the employer, it is possible that the tax administration claims to the taxpayer already deducted at source a supplement of tax, if the person already subject to other income such as an incidental activity, income from securities, possession of immovables.</p>
                                </section>
