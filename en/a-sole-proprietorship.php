                            <div class="fancy-title left-align"><h1>A sole proprietorship</h1></div>
                            <div class="services-wrap">
                                <p>A sole proprietorship is the perfect choice for a small business or an ancillary activity, but this does not prevent certain professionals such as doctors, plumbers, garages, restaurants, home workers, etc. from adopting this type of legal form, considering it more appropriate to their activity.</p>
                                <div class="fancy-title left-align"><h2>What are the advantages and disadvantages?</h2></div>
                                <h3 class="section-title sub">Advantages</h3>
                                <ul>
                                    <li>No particular formality, the person can start his activity very quickly and no legal constraints are imposed on him.</li>
                                    <li>No base capital is mandatory, this means that the person has the right to start at even zero Franc.</li>
                                    <li>The deduction of all expenses related directly and indirectly to the sole proprietorship. </li>
                                    <li>No double taxation as in SA or SARL.</li>
                                    <li>Possibility to change legal status and switch to a SARL or a SA. </li>
                                    <li>Obligation to register in the commercial register only if the turnover exceeds CHF 100,000.</li>
                                </ul>
                                <h3 class="section-title sub">Disadvantages</h3>
                                <ul>
                                    <li>Unlimited liability (civil and criminal), the debts of the company pursue the owner throughout his life, which means that there is no barrier between the personal assets of the entrepreneur and the capital of the company, namely that the capital and income of the company are added to the assets and other household income of married couples and registered partners to make a single tax return, this makes the spouse or registered partner of the company liable in turn for tax and commercial debts. </li>
                                    <li>Inability to access capital markets.</li>
                                    <li>Loss of anonymity after entry in the commercial register. </li>
                                </ul>
                                <div class="fancy-title left-align"><h2>General partnership</h2></div>
                                <p>A general partnership, also known as a "partnership", is the product of an association between two or more persons: this legal status is rather adapted to very small structures grouping more than one person, such as a hair salon, a small taxi company, equipment rental, etc.</p>
                                <p>The advantages and disadvantages are more or less the same compared to a sole proprietorship: </p>
                                <h3 class="section-heading">Advantages</h3>
                                <ul>
                                    <li>No particular formality, people can start their activity very quickly.</li>
                                    <li>No base capital is required, which means that people are entitled to start at even zero Franc.</li>
                                    <li>The deduction of all expenses directly and indirectly related to the activity.</li>
                                    <li>No double taxation as in SA or SARL.</li>
                                    <li>Possibility to change legal status and switch to a SARL or SA.</li>
                                </ul>
                                <h3 class="section-heading">Disadvantages</h3>
                                <ul>
                                    <li>The greatest disadvantage of a general partnership (SNC) is profit sharing: according to the law, SNC owners share the company's profit fairly even if the capital contribution of one of the owners remains higher than the other. This could be avoided by drafting a contract for profit sharing before the creation of the company. Once the company has been launched, no changes on profit sharing are possible. </li>
                                    <li>The other disadvantage that once again makes the SNC less desirable from our point of view is none other than the unlimited liability (civil and criminal) of the partners. The company's debts pursue the owners throughout their lives, which means that there is no barrier between the personal assets of the entrepreneurs and the company's capital, i.e. the company's capital and income are added to the partners' assets and other income so that everyone can file a tax return on their side. </li>
                                    <li>Inability to access capital markets. </li>
                                    <li>Loss of anonymity after registration in the commercial register.</li>
                                </ul>
                                <p><strong>Note¹:</strong> In general, we advise all our clients who want to create a SNC to choose a SARL instead if they have a capital of CHF 20,000 or more, the SARL’s liability limit protects each entrepreneur in the event of bankruptcy or litigation.  </p>
