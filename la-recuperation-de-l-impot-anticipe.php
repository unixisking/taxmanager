						<div class="fancy-title left-align"><h1>La récupération de l’impôt anticipé </h1></div>
                            <div class="services-wrap">
                                <p>L’impôt anticipé est un impôt de 35 % prélevé sur le revenu des biens mobiliers tels que : dividendes, intérêts des capitaux, parts de bénéfice, gains en loterie… Puis verser à l’administration fédérale de contribution. Cet impôt n’est pas définitif et peut être récupéré dans certains cas : nous allons essayer de vous expliquer brièvement le fonctionnement de cette imposition et dans lequel il pourra être récupéré.</p>
                                <h3 class="section-title sub">Pour les résidents suisses (personnes physiques) </h3>
                                <p>L’impôt anticipé n’est prélevé que pour garantir le paiement du revenu relatif à ce gain, au moment de la déclaration d’impôts ordinaire ou simplifiée. Il sera évidemment reporté avec le revenu pour lequel l’impôt anticipé a été retenu. Dans ce cas, l’administration fiscale prendra en compte cet impôt prélevé au préalable afin de le rembourser totalement ou partiellement, sinon il sera additionné aux acomptes du contribuable. À noter que l’impôt anticipé n’est remboursé que sous certaines conditions. </p>
                                <div class="center"><img src="images/shape1-fr.jpg" alt=""></div>
                                <h3 class="section-title sub">Pour les personnes morales en Suisse SARL, SA…</h3>
                                <p>Pour les personnes morales en Suisse, la récupération de l’impôt est plus simple et se fait directement auprès de l’administration fédérale de contribution contrairement aux personnes physiques qui le réclament auprès de l’administration cantonale de contribution. </p>
                                <div class="center"><img src="images/shape2-fr.jpg" alt=""></div>
                                <h5 class="section-title sub">Pour les personnes physiques et morales résidant ou ayant leurs sièges sociaux à l’étranger</h5>
                                <p>Dans ce cas, le remboursement partiel de l’impôt anticipé dépend de la convention de double imposition passée avec les autorités suisses. Le remboursement de l’impôt anticipé se fait seulement si le bénéficiaire des intérêts ou dividendes a déclaré ce revenu à l’autorité fiscale auquel est rattachée la personne physique ou morale. L’administration fédérale de contribution ne procède à aucun remboursement jusqu’à ce qu’elle soit notifiée par l’administration fiscale étrangère lui confirmant que le revenu a été déclaré. S’il n’existe aucune convention, le remboursement partiel de l’impôt anticipé est impossible.</p>
                                <div class="center"><img src="images/shape3-fr.jpg" alt=""></div>
                                <p><strong>Remarque ¹:</strong> selon l’article 5 de la loi fédérale sur l'impôt anticipé, les intérêts bancaires des clients n’excédant pas 200 CHF pour toute une année civile sont exonérés de l’impôt anticipé. </p>
                                <p><strong>Remarque ²:</strong> selon l’article 24 de loi fédérale sur l'impôt fédéral direct, l’impôt anticipé n’est plus prélevé depuis 2013 sur les gains en loterie supérieurs ou égaux à <strong>1’000 CHF</strong>.</p>
