                                <div class="fancy-title left-align"><h2>Entreprise ou raison individuelle :</h2></div>
                                <p>La raison individuelle est le choix parfait pour un petit commerce ou une activité accessoire, cela n’empêche pas que certains professionnels tels que le médecin, plombier, garagiste, restaurateur, travailleur à domicile… Adoptent ce type de forme juridique, jugeant qu’il correspond mieux à leur activité.</p>
                                <div class="fancy-title left-align"><h2>Quels sont les avantages et les inconvénients ?</h2></div>
                                <h3 class="section-title sub">Avantages:</h3>
                                <ul>
                                    <li>Aucune formalité particulière, la personne peut commencer son activité très rapidement et aucune contrainte juridique ne lui est imposée. </li>
                                    <li>Aucun capital de base n’est obligatoire, cela veut dire que la personne a le droit de commencer à même zéro Franc. </li>
                                    <li>La déduction de toutes les charges liées directement et indirectement à l’entreprise individuelle. </li>
                                    <li>Pas de doubles impositions comme dans les SA ou SARL.</li>
                                    <li>Possibilité de changer de statut juridique et de basculer à une SARL ou SA. </li>
                                    <li>Obligation de s’inscrire au registre de commerce seulement si le chiffre d’affaires dépasse 100’000 CHF. </li>
                                </ul>
                                <h3 class="section-title sub">Inconvénients:</h3>
                                <ul>
                                    <li>Responsabilité illimitée (civile et pénale), les dettes de l’entreprise poursuivent le propriétaire tout au long de sa vie, ce qui veut dire qu’il n’existe aucune barrière entre le patrimoine personnel de l’entrepreneur et le capital de l’entreprise, à savoir que le capital et le revenu de l’entreprise sont additionnés au patrimoine et aux autres revenus du ménage des couples mariés et partenaires enregistrés pour faire une seule déclaration d’impôts, cela rend le conjoint ou le partenaire enregistré de l’entreprise responsable à son tour des dettes fiscales et commerciales. </li>
                                    <li>Impossibilité d’accéder aux marchés des capitaux.</li>
                                    <li>Perte d’anonymat après l’inscription au registre de commerce. </li>
                                </ul>
                                <div class="fancy-title left-align"><h2>Société en nom collectif:</h2></div>
                                <p>La société en nom collectif aussi appelé « sociétés de personnes » est le produit de l’association entre deux ou plusieurs personnes : ce statut juridique est plutôt adapté à des toutes petites structures regroupant plus d’une personne telles qu’un salon de coiffure, une petite société de taxi, la location de matériel…</p>
                                <p>Les avantages et inconvénients sont à quelques points près de l’entreprise individuelle: </p>
                                <h3 class="section-heading">Avantages:</h3>
                                <ul>
                                    <li>Aucune formalité particulière, les personnes peuvent commencer leur activité très rapidement.</li>
                                    <li>Aucun capital de base n’est requis, cela veut dire que les personnes ont le droit de commencer à même zéro Franc. </li>
                                    <li>La déduction de toutes les charges liées directement et indirectement à l’activité. </li>
                                    <li>Pas de doubles impositions comme dans les SA ou SARL.</li>
                                    <li>Possibilité de changer de statut juridique et de basculer à une SARL ou SA.</li>
                                </ul>
                                <h3 class="section-heading">Inconvénients:</h3>
                                <ul>
                                    <li>Le plus grand inconvénient de la société en nom collectif est le partage du bénéfice : selon la loi, les titulaires d’une SNC partagent équitablement le bénéfice de l’entreprise même si l’apport en capital d’un des propriétaires reste supérieur au deuxième. Cela pourrait être évité par la rédaction d’un contrat pour le partage des bénéfices avant la création de l’entreprise. Une fois que l’entreprise a été lancée, aucun changement sur le partage du bénéfice n’est possible.</li>
                                    <li>L’autre inconvénient qui rend encore une fois la SNC moins désirable selon notre point de vue, n’est autre que la responsabilité illimitée (civile et pénale) des associés. Les dettes de l’entreprise poursuivent les propriétaires tout au long leur vie, ce qui veut dire qu’il n’existe aucune barrière entre le patrimoine personnel des entrepreneurs et le capital de l’entreprise, à savoir que le capital et le revenu de l’entreprise sont additionnés aux patrimoines et aux autres revenus des associés pour que chacun dépose une déclaration d’impôts de son côté.</li>
                                    <li>Impossibilité d’accéder aux marchés des capitaux. </li>
                                    <li>Perte d’anonymat après l’enregistrement au registre de commerce.</li>
                                </ul>
