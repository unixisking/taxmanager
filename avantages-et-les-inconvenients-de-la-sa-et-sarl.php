                            <div class="services-wrap">
                                <p>Selon les articles 772-827 du code des obligations, la société à responsabilité limitée est considérée en tant que société de capitaux, elle peut être créé par une ou plusieurs personnes physiques ou morales ou les deux à la fois : les dettes ne sont garanties que par le total de l’actif de l’entreprise.</p>
                                <p>La création d’une SARL nécessite un capital minimum de 20’000 CHF, sans oublier que les frais de création qui varient entre 2’500et 4’500 CHF doivent obligatoirement passer par un notaire, à noter que l’inscription au registre de commerce est obligatoire et doit se faire là où elle a son siège principal, les succursales doivent elle aussi être inscrites au lieu où elles sont situées. </p>
                                <p>La société à responsabilité limitée acquiert la personnalité juridique qu’au moment de son inscription au registre du commerce.</p>
                                <div class="fancy-title left-align"><h2>Quels sont les avantages et les inconvénients de la Sàrl ?</h2></div>
                                <h3 class="section-title sub">Avantages:</h3>
                                <ul>
                                    <li>La responsabilité est limitée au capital de l’entreprise. </li>
                                    <li>Peut être créé par une seule personne physique ou morale.</li>
                                    <li>La liberté de choisir le nom de l’entreprise (La mention « Sàrl » est obligatoire). </li>
                                    <li>Possibilité de changer le statut juridique en une société anonyme. </li>
                                    <li>Capital minimum de 20’000 CHF. </li>
                                    <li>Le ou les associés peuvent recevoir un salaire qui est considéré comme charge pour l’entreprise, ce qui fait automatiquement réduire la charge fiscale de la Sàrl. </li>
                                    <li>Le bénéfice perçu par la vente de parts sociales n’est pas imposable.</li>
                                    <li>Possibilité d’augmenter le capital en ajoutant des investisseurs. </li>
                                </ul>
                                <h3 class="section-title sub">Inconvénients:</h3>
                                <ul>
                                    <li>La double imposition (impôt sur le bénéfice et le capital de la Sarl puis impôt sur le revenu des dividendes et du capital pour le ou les titulaires des parts sociales de la Sàrl).</li>
                                    <li>Les frais de création de la Sàrl (entre 2500 et 4500 CHF).</li>
                                    <li>La publication au registre du commerce de l’identité et du capital du ou des propriétaire(s) de la Sàrl. </li>
                                    <li>Frais de gestion plus élevés qu’une entreprise individuelle ou une société en nom collectif. </li>
                                    <li>Aucun droit aux indemnités chômage pour les propriétaires de la Sàrl, sauf s’ils quittent définitivement l’entreprise.</li>
                                </ul>
                                <h3 class="section-title sub">La société anonyme:</h3>
                                <p>Le choix de la société anonyme est plutôt adapté aux grandes et moyennes entreprises dont le capital dépasse 100’000 CHF. La plus grande différence entre une société anonyme et une société à responsabilité limitée est le capitale initial et l’anonymat des actionnaires. </p>
                                <p>Selon l’article 620 du code des obligations, la société anonyme se forme sous une raison sociale dont le capital action est déterminé à l’avance et divisé en actions, les dettes de la société anonyme ne sont garanties que par le total de l’actif de l’entreprise.</p>
                                <p>La société anonyme peut être créée par une ou plusieurs personnes physiques ou morales, elle peut aussi être créée pour un but non lucratif. </p>
                                <p>La société anonyme a le droit d’émettre deux types d’actions : les actions nominatives et les actions au porteur. Les actions nominatives sont inscrites au registre des actionnaires de la société concernée, c’est-à-dire que la société connaît le nom, prénom, date de naissance et adresse de l’actionnaire : pour les actions au porteur, elles ne sont pas libellées au nom du détenteur, ce qui veut dire que la société ne connaît pas qui détient l’action au porteur. </p>
                                <p>Comme pour la société à responsabilité limitée, l’inscription au registre du commerce de la société anonyme est obligatoire dans le lieu où elle a son siège, les succursales doivent à leur tour être inscrites au registre de commerce du lieu où elles sont situées.</p>
                                <p>La société anonyme acquiert la personnalité juridique qu’au moment de son inscription au registre du commerce.</p>
