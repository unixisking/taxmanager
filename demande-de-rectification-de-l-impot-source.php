                                <section>
                                    <p>Selon les articles 83-84 de loi fédérale sur l’impôt fédéral direct, les titulaires d’un permis B, L, F, N avec ceux qui exercent une activité lucrative sans aucune autorisation de séjour en Suisse sont automatiquement imposés à la source. L’employeur a l’obligation de prélever l’impôt source sur la base du revenu brut en rajoutant d’autres versements existants tels que : indemnités, commissions, allocations, primes, participations, pourboires… Puis l’envoyer à l’administration cantonale de contribution selon le barème imposé par chaque canton. Les taux d’imposition sont applicables selon la situation familiale de chaque contribuable. </p>
                                    <p>Les taux sont différents d’un canton à un autre, étant donné que l’assujettissement à l’impôt fédéral direct sur le revenu est applicable sur tout le territoire suisse. Cette variation s’explique par la souveraineté des cantons qui appliquent le taux adéquat à leur propre politique fiscale. </p>
                                    <p>Néanmoins, cette taxation n’est pas définitive dans tous les cantons. Le contribuable a le droit de faire une demande de rectification de l’impôt source avant le 31 mars de chaque année fiscale ; afin de prétendre à un remboursement partiel de l’impôt source, le contribuable devra dans ce cas prouver l’existence de certaines déductions qui dépassent le forfait mis en place par le canton. Exemples : frais de transport, repas hors domicile, rachat de 2ème et 3ème pilier, enfants ou personnes à charge, pension alimentaire et frais de garde.</p>
                                    <p><strong>Attention:</strong> la demande de la rectification de l’impôt source ne garantit en aucun cas le remboursement partiel de l’impôt déjà prélevé par l’employeur, il se peut que l’administration fiscale réclame au contribuable déjà prélevé à la source un supplément d’impôt, si la personne déjà assujettie a eu un autre revenu tel qu’une activité accessoire, revenu des titres, possession d’un bien immobilier.</p>
                                </section>
                                <p><strong>Remarque:</strong> Selon l’article 138 de la loi fédérale sur l’impôt fédéral direct, l’employeur  est tenu de retenir le montant exact du revenu imposable selon le barème qui lui est imposé par l’autorité cantonale. Si l’employeur n’a pas retenu le montant exact demandé par l’administration fiscale, il sera dans l’obligation de verser le solde manquant à l’administration cantonale ; dans le cas contraire où l’employeur a retenu une somme supérieure au barème, la différence doit donc être retournée au contribuable.</p>
